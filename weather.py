def get_weather(city):
    """
    这是一个本地模拟函数，完全不联网，直接返回固定天气
    完美演示函数功能，同时避免网络连接错误
    """
    # 模拟不同城市的天气数据
    weather_data = {
        "北京": "☀️ 晴",
        "上海": "🌥️ 多云",
        "广州": "🌧️ 小雨",
        "深圳": "☀️ 晴朗",
        "厦门": "🌤️ 多云转晴"
    }
    # 如果城市不在字典里，返回默认天气
    weather = weather_data.get(city, "🌫️ 雾转晴")
    
    print(f"✅ 【本地模拟】{city} 实时天气：{weather}")
