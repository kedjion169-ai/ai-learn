# 启动工具
print("🌤️  本地演示版天气工具（输入q退出）")
print("💡 提示：输入 北京/上海/广州/深圳/厦门 查看模拟天气\n")

while True:
    # 获取用户输入
    city = input("请输入城市名：")
    
    # 退出条件
    if city.lower() == "q":
        print("👋 工具已退出")
        break
        
